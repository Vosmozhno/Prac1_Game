def display_stats(player):
    max_health = player['Максимальное здоровье']
    health = player['Здоровье']
    max_psychic = player['Максимальная психика']
    psychic = player['Психика']
    max_defense = player['Максимальная защита']
    defense = player['Защита']
    max_weight = player['Максимальный вес']
    weight = player['Вес']

    bars = 15

    red = "\033[91m"
    blue = "\033[94m"
    yellow = "\033[93m"
    green = "\033[92m"
    deafult = "\033[0m"

    remaining_health_bars = round(health / max_health * bars)
    lost_health_bars = bars - remaining_health_bars

    remaining_psychic_bars = round(psychic / max_psychic * bars)
    lost_psychic_bars = bars - remaining_psychic_bars

    remaining_defense_bars = round(defense / max_defense * bars)
    lost_defense_bars = bars - remaining_defense_bars

    remaining_weight_bars = round(weight / max_weight * bars)
    lost_weight_bars = bars - remaining_weight_bars

    print(f"ЗДОРОВЬЕ: {health} / {max_health}")
    print(f"|{red}{remaining_health_bars * '█'}{lost_health_bars * '▒'}{deafult}|")
    print(f"ВЕС: {weight} / {max_weight}")
    print(f"|{blue}{remaining_weight_bars * '█'}{lost_weight_bars * '▒'}{deafult}|")
    print(f"ПСИХИКА: {psychic} / {max_psychic}")
    print(f"|{yellow}{remaining_psychic_bars * '█'}{lost_psychic_bars * '▒'}{deafult}|")
    print(f"ЗАЩИТА: {defense} / {max_defense}")
    print(f"|{green}{remaining_defense_bars * '█'}{lost_defense_bars * '▒'}{deafult}|")
    next_stats()

