def stock_exchange(player):
    previous_graph = None  # Предыдущее значение графика

    # Проверка наличия ключа "Счет биржи" в словаре игрока
    if "Счет биржи" not in player:
        player["Счет биржи"] = 0  # Установка начального значения счета биржи

    while True:
        graph = [random.randint(1, 20) for _ in range(6)]  # 6 случайных значений

        # Добавление предыдущего значения в график
        if previous_graph is not None:
            graph.insert(0, previous_graph)

        # Обновление графика случайным образом
        if random.choice([True, False]):
            graph.append(graph[-1] + random.randint(1, 10))
        else:
            graph.append(graph[-1] - random.randint(1, 10))

        # Отображение графика биржи на одной строке
        for value in graph[-6:]:
            if value > graph[-7]:
                print("/", end="")
            else:
                print("\\", end="")

        print(f"{green}\nВаш баланс:", player["Количество денег"])
        print(f"{green}Баланс счета биржи:", player["Счет биржи"])

        # Предложение игроку внести или вывести деньги
        choice = input(f"{green}Выберите действие (внести/вывести/выйти): ")

        if choice == "внести":
            amount = int(input("Введите сумму для внесения: "))
            if amount > 0:
                player["Счет биржи"] += amount
                print("Вы внесли", amount, "на счет биржи.")
            else:
                print(f"{green}Введите положительную сумму для внесения.")
        elif choice == "вывести":
            amount = int(input("Введите сумму для вывода: "))
            if amount > 0 and amount <= player["Счет биржи"]:
                player["Счет биржи"] -= amount
                print(f"{green}Вы вывели", amount, "со счета биржи.")
            else:
                print(f"{green}У вас недостаточно средств на счете биржи или введена некорректная сумма.")
        elif choice == "выйти":
            dom()
        else:
            print("Неправильный выбор. Попробуйте еще раз.")

        print()

        # Сохранение предыдущего значения графика
        previous_graph = graph[-1]

    print(f"{green}Предыдущее значение графика:", previous_graph)

