def buy_weapon():
    available_weapons = random.sample(weapons, 4)
    print_slowly(f"{blue}Оружейник:{deafult}Выбирай оружие для покупки:")
    for index, weapon in enumerate(available_weapons):
        time.sleep(0.5)
        print_slowly(f"{index + 1}. {weapon['название']}, атака: {weapon['Атака']}, цена: {weapon['стоимость']}")
    print_slowly(f"{deafult}[{yellow}0{deafult}] Выйти из магазина")
    while True:
        weapon_choice = input("Введите номер оружия или 0 для выхода: ")
        if weapon_choice == "0":
            print_slowly(f"{blue}Оружейник:{deafult} Ты приходи ежели что-то нужно!")
            break
        if weapon_choice.isdigit():
            weapon_index = int(weapon_choice) - 1
            if 0 <= weapon_index < len(available_weapons):
                if player["Количество денег"] >= available_weapons[weapon_index]["стоимость"]:
                    # Проверяем случайную скидку
                    discount_chance = random.randint(1, 100)
                    if discount_chance <= 5 and len(player["Скидка на оружие"]) < 2:
                        discount = random.choice([5, 10, 15, 20])
                        player["Скидка на оружие"].append(available_weapons[weapon_index])
                        discounted_price = available_weapons[weapon_index]["стоимость"] - (
                                available_weapons[weapon_index]["стоимость"] * discount / 100)
                        print_slowly(f"Вы получили скидку {discount}% на {available_weapons[weapon_index]['название']}!")
                        print_slowly(f"Цена с учетом скидки: {discounted_price} денег.")
                        player["Количество денег"] -= discounted_price
                    else:
                        player["Количество денег"] -= available_weapons[weapon_index]["стоимость"]
                        player["Ваши вещи"].append(available_weapons[weapon_index])
                        player["Оружие"].append(available_weapons[weapon_index])
                        print_slowly(f"Вы купили {available_weapons[weapon_index]['название']} за {available_weapons[weapon_index]['стоимость']} денег.")
                else:
                    print_slowly(f"{blue}Оружейник:{deafult}Денег у тебя не хватит купить такую вещь!")
                    buy_weapon()
            else:
                print_slowly("Некорректный выбор оружия.")
        else:
            print_slowly("Некорректный ввод. Введите номер оружия или 0 для выхода.")


